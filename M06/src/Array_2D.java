
public class Array_2D {

	public static void main(String[] args) {
	
		int[][] x =  new int[2][3];
		
		System.out.println(x);		//prints the address of array x
		
		System.out.println(x[0]);
		
		System.out.println(x[0][0]); //prints the default value of array
		
		System.out.println(x[0][1]);

	}

}
